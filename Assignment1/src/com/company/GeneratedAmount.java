package com.company;

public final class GeneratedAmount {

    final int numberOfTrucks;
    final int numberOfShips;
    final int numberOfContainers;

    public GeneratedAmount(int numberOfTrucks, int numberOfShips, int numberOfContainers){
        this.numberOfTrucks = numberOfTrucks;
        this.numberOfShips = numberOfShips;
        this.numberOfContainers = numberOfContainers;
    }


}
